x = 10
y = 5 

z = x + y # addirtion
print(z)  # Output: 15

z = x - y #subtraction
print(z)  # Output: 5

z = x * y # multiplication
print(z)  # Output: 50

z = x / y # division
print(z)  # Output: 2.0
print(type(z))  # Output: <class 'float'>2

z = x % y # modulus
print(z)  # Output: 0
print(type(z))  # Output: <class 'int'>

z = x // y # floor division
print(z)  # Output: 2
print(type(z))  # Output: <class 'int'>
